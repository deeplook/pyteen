def test_20():
    # fmt: off
    assert list(pi_digits(20)) == [
        3, 1, 4, 1, 5, 9, 2, 6, 5, 3,
        5, 8, 9, 7, 9, 3, 2, 3, 8, 4
    ]
    # fmt: on


